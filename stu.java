public class stu {
    private String name;
    private String address;

    public stu(String name, String address) {


        this.name = name;
        this.address = address;
    }


    public String getName() {

        return this.name;
    }

    public String getAddress() {
        return this.address;
    }


    public void setName(String nm) {
        this.name = nm;
    }

    public void setAddress(String add) {
        this.address = add;
    }


}